# Data Types in Python
a = 1
b = 2.3
c = "Hello"
d = True
e = None
f = 1+2j
g = [1, 2.4, "Hello", False, None, 1+2j]
h = (1, 4, "World", True, None, 3+4j)

print("Type of a is", type(a))
print("Type of b is", type(b))
print("Type of c is", type(c))
print("Type of d is", type(d))
print("Type of e is", type(e))
print("Type of f is", type(f))
print("Type of g is", type(g))
print("Type of h is", type(h))

# ------------Data Types in Python-----------------
# Type of a is <class 'int'>
# Type of b is <class 'float'>
# Type of c is <class 'str'>
# Type of d is <class 'bool'>
# Type of e is <class 'NoneType'>
# Type of f is <class 'complex'>
# Type of g is <class 'list'>
# Type of h is <class 'tuple'>